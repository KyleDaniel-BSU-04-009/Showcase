var projectsL = JSON.parse(JSON.stringify(projectsList));
var projectCollection = document.querySelectorAll('.projectCollection');
for(var i = 0; i < projectsL.length; i++){
  for(var j = 0; j < projectsL[i].projects.length; j++){
    var newProject = document.createElement('div');
    newProject.classList.add('project');
    projectCollection[i].appendChild(newProject);
  
    var projectsBar = projectCollection[i].querySelectorAll('.project');
    var projectIcons = document.createElement('div');
    projectIcons.classList.add('projectIcons');
    projectsBar[j].appendChild(projectIcons);
    
    var icons = projectsBar[j].querySelector('.projectIcons');
    for(var k = 0; k < projectsL[i].projects[j].icons.length; k++){
      var newIcon = document.createElement('img');
      newIcon.src = `images/icons/${projectsL[i].projects[j].icons[k]}.png`;
      icons.appendChild(newIcon);
    }
    var newTitle = document.createElement('h1');
    newTitle.textContent = projectsL[i].projects[j].name;
    projectsBar[j].appendChild(newTitle);
    var newSubtitle = document.createElement('h2');
    newSubtitle.textContent = projectsL[i].projects[j].subtitle;
    projectsBar[j].appendChild(newSubtitle);
    var newImg = document.createElement('img');
    newImg.src = `images/projects/${projectsL[i].projects[j].name.toLowerCase().replaceAll(' ', '_')}.png`;
    projectsBar[j].appendChild(newImg);
    var newP = document.createElement('p');
    newP.textContent = projectsL[i].projects[j].description;
    projectsBar[j].appendChild(newP);
    
    var newA = document.createElement('a');
    newA.href = projectsL[i].projects[j].link.data;
    newA.textContent = 'view';
    
    if(projectsL[i].projects[j].link.type == 'documentation'){ newA.style.filter = 'hue-rotate(335deg)'; }
    if(projectsL[i].projects[j].link.type == 'video'){ newA.style.filter = 'hue-rotate(112deg) contrast(1.6)'; }
    if(projectsL[i].projects[j].link.type == 'website'){ newA.style.filter = 'hue-rotate(46deg)'; }
    if(projectsL[i].projects[j].link.type == 'itch'){ newA.style.filter = 'hue-rotate(119deg)'; }
    if(projectsL[i].projects[j].link.type == 'drive'){ newA.style.filter = 'hue-rotate(254deg)'; }
    
    projectsBar[j].appendChild(newA);
  }
}
